def get_template_prompt_enhancement(template_name: str) -> str:
    """
    根据模板名称返回增强的 prompt
    """
    if template_name not in VIRAL_TEMPLATES:
        return ""
    
    template = VIRAL_TEMPLATES[template_name]
    return f"""
【爆款模板：{template.name}】
{template.description}

{template.structure}

{template.tips}

请严格按照以上模板结构创作分镜脚本！
"""


def get_available_templates() -> list:
    """返回所有可用的模板名称"""
    return list(VIRAL_TEMPLATES.keys())


def get_template_description(template_name: str) -> str:
    """返回模板的简短描述"""
    if template_name not in VIRAL_TEMPLATES:
        return "未知模板"
    return VIRAL_TEMPLATES[template_name].description
